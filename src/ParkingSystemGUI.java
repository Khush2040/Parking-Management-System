    private void btnParkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnParkActionPerformed
        String number = txtVehicleNumber.getText();
        String owner = txtOwnerName.getText();
        String type = cmbVehicleType.getSelectedItem().toString();

        if (number.isEmpty() || owner.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill all fields.");
        return;
        }

// Check for duplicates
        for (Vehicle v : parkedList) {
            if (v.getNumber().equalsIgnoreCase(number)) {
                JOptionPane.showMessageDialog(this, "Vehicle already parked!");
                return;
                }
           }

// Check parking limit
           if (parkedList.size() >= TOTAL_SLOTS) {
                JOptionPane.showMessageDialog(this, "Parking full!");
                return;
            }

// Record current time
            String time = new SimpleDateFormat("HH:mm:ss").format(new Date());
            Vehicle v = new Vehicle(number, owner, type, time);
            parkedList.add(v);

// Add to table
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            model.addRow(new Object[]{number, owner, type, time});

            updateDashboard();
            clearFields();

    }//GEN-LAST:event_btnParkActionPerformed

    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int row = table.getSelectedRow();
if (row != -1) {
    parkedList.remove(row);
    ((DefaultTableModel) table.getModel()).removeRow(row);
    updateDashboard();
} else {
    JOptionPane.showMessageDialog(this, "Please select a row to remove.");
}

    }//GEN-LAST:event_btnRemoveActionPerformed

    private void btnClearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnClearActionPerformed
        clearFields();
}
private void clearFields() {
    txtVehicleNumber.setText("");
    txtOwnerName.setText("");
    cmbVehicleType.setSelectedIndex(0);
}
private void updateDashboard() {
    lblOccupied.setText("Occupied: " + parkedList.size());
    lblAvailable.setText("Available: " + (TOTAL_SLOTS - parkedList.size()));


    }//GEN-LAST:event_btnClearActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
      String query = txtSearch.getText().toLowerCase();
DefaultTableModel model = (DefaultTableModel) table.getModel();
model.setRowCount(0); // Clear old rows

for (Vehicle v : parkedList) {
    if (v.getNumber().toLowerCase().contains(query) ||
        v.getOwner().toLowerCase().contains(query) ||
        v.getType().toLowerCase().contains(query)) {
        model.addRow(new Object[]{v.getNumber(), v.getOwner(), v.getType(), v.getTime()});
    }
}
  
    }//GEN-LAST:event_btnSearchActionPerformed

